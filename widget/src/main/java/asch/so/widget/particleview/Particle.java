package asch.so.widget.particleview;

/**
 * @author kimziv
 */
public class Particle {

    public float x;
    public float y;
    public float radius;

    public Particle() {
    }

    public Particle(float x, float y, float radius) {
        this.x = x;
        this.y = y;
        this.radius = radius;
    }
}
